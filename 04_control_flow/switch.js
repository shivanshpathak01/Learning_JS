// How to check the Strings in Switch statement 

const month = "hello";
switch (month) {
    case "jan":
        console.log("January");
        break;
    case "dec":
        console.log("December");
        break;
    case "aug":
        console.log("August");
        break;
    case "feb":
        console.log("February");
        break;

    default: "Arrival!!!"
        break;
}